async def seed() -> None:
    async with SessionFactory() as session:
        # Проверяем, не засеяно ли уже
        result = await session.execute(text("SELECT COUNT(*) FROM articles"))
        count = result.scalar()
        if count and count > 0:
            print(f"[seed] Таблица articles уже содержит {count} записей. Пропускаем.")
            return

        for data in ARTICLES:
            row = {
                "id": __import__("uuid").uuid4(),
                "content_type": data["content_type"],
                "title": data["title"],
                "slug": data["slug"],
                "excerpt": data.get("excerpt"),
                "body": data.get("body", ""),
                "cover_image": data.get("cover_image"),
                "is_published": True,
                "published_at": data.get("published_at"),
                "views_count": __import__("random").randint(50, 1500),
                "promo_discount_pct": data.get("promo_discount_pct"),
                "promo_valid_until": data.get("promo_valid_until"),
                "author_name": data.get("author_name"),
                "tags": None,
                "created_at": now,
                "updated_at": now,
            }
            await session.execute(
                text(
                    "INSERT INTO articles "
                    "(id, content_type, title, slug, excerpt, body, cover_image, "
                    "is_published, published_at, views_count, promo_discount_pct, "
                    "promo_valid_until, author_name, tags, created_at, updated_at) "
                    "VALUES (:id, :content_type, :title, :slug, :excerpt, :body, :cover_image, "
                    ":is_published, :published_at, :views_count, :promo_discount_pct, "
                    ":promo_valid_until, :author_name, :tags, :created_at, :updated_at)"
                ),
                row,
            )
            print(f"[seed] + {data['content_type']:5s} | {data['title'][:55]}")

        await session.commit()
        print(f"[seed] Готово! Добавлено {len(ARTICLES)} записей.")


if __name__ == "__main__":
    asyncio.run(seed())
